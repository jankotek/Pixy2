	/**
	 * Opens a catalog. This method must be invoked at first.
	 * @param coor the R.A. and Decl. of the center.
	 * @param fov  the field of view to read in degree.
	 * @exception IOException if a file cannot be accessed.
	 * @exception FileNotFoundException if a file does not exists in
	 * any URL, and the file is in any CD-ROMs.
	 * @exception CdromNotFoundException if a file does not exists in
	 * any URL, and the file is in a CD-ROM.
	 */
	public void open ( Coor coor, double fov )
		throws IOException, FileNotFoundException, CdromNotFoundException
	{
		center_coor = coor;
		circum_area = null;
		if (coor != null)
			circum_area = new CircumArea(coor, fov / 2.0);
		file_list = new Vector();

		// Select files to read.
		for (int i = 0 ; i < file_N ; i++) {
			Coor start_coor = new Coor(0, file_decl_start[i]);
			Coor end_coor = new Coor(180, file_decl_end[i]);

			if (circum_area == null  ||  circum_area.overlapsArea(start_coor, end_coor)) {
				file_list.addElement(new Integer(i));
			} else {
				start_coor = new Coor(180, file_decl_start[i]);
				end_coor = new Coor(0, file_decl_end[i]);

				if (circum_area == null  ||  circum_area.overlapsArea(start_coor, end_coor))
					file_list.addElement(new Integer(i));
			}
		}

		// Initializes to read data one by one.
		current_reader = null;
	}

	/**
	 * Reads one data from the opened catalog. After this method is 
	 * invoked, the cursor is promoted to tne next star. When every 
	 * data is read, it returns null. Note that data with the same ID
	 * are also returned one by one.
	 * @return a star data.
	 * @exception IOException if a file cannot be accessed.
	 * @exception FileNotFoundException if a file does not exists in
	 * any URL, and the file is in any CD-ROMs.
	 * @exception CdromNotFoundException if a file does not exists in
	 * any URL, and the file is in a CD-ROM.
	 * @exception QueryFailException if the query to the server is 
	 * failed.
	 */
	public CatalogStar readNext ( )
		throws IOException, FileNotFoundException, CdromNotFoundException, QueryFailException
	{
		while (true) {
			try {
				// In the case of the very first reading.
				if (current_reader == null) {
					current_file_index_in_list = -1;
					throw new EOFException();
				}

				current_star_index++;

				// In the case it reaches to the end of a block.
				if (current_star_index == 2000) {
					current_block_index++;

					// In the case it reaches to the last block.
					if (current_block_index == file_block_count[current_file_index])
						throw new EOFException();

					Coor start_coor = new Coor((double)ra_start[current_file_index][(int)current_block_index], (double)decl_start[current_file_index][(int)current_block_index]);
					Coor end_coor = new Coor((double)ra_end[current_file_index][(int)current_block_index], (double)decl_end[current_file_index][(int)current_block_index]);
					if (circum_area == null  ||  circum_area.overlapsArea(start_coor, end_coor)) {
						// In the case within the field.
						current_star_index = 0;
					} else {
						// In the case not within the field.

						// In the case it reaches to the last block.
						if (current_block_index + 1 == file_block_count[current_file_index])
							throw new EOFException();

						// Skips to the next block.
						StreamSkipTimer skip_timer = new StreamSkipTimer(current_reader, 10000);
						skip_timer.skip(2000 * 81);

						current_star_index = 2000 - 1;

						continue;
					}
				}

				String record = current_reader.readLine();
				if (record == null)
					throw new EOFException();

				if (record.substring(0, 10).trim().length() > 0) {
					double ra = Format.doubleValueOf(record.substring(10, 20)) / 100.0 / 3600.0;
					double decl = Format.doubleValueOf(record.substring(20, 29)) / 100.0 / 3600.0 - 90.0;

					Coor coor = new Coor(ra, decl);

					if (circum_area == null  ||  circum_area.inArea(coor)) {
						short region = Short.parseShort(record.substring(0, 4).trim());
						short number = Short.parseShort(record.substring(5, 10).trim());

						TicStar star = new TicStar(region, number, coor);

						if (record.substring(38, 43).trim().length() > 0) {
							int b_mag = Integer.parseInt(record.substring(38, 43).trim());
							if (b_mag > 0)
								star.setBMagnitude((double)b_mag / 100.0);
						}

						if (record.substring(47, 52).trim().length() > 0) {
							int v_mag = Integer.parseInt(record.substring(47, 52).trim());
							if (v_mag > 0)
								star.setVMagnitude((double)v_mag / 100.0);
						}

						if (star.getMag() <= limiting_mag) {
							if (center_coor != null) {
								ChartMapFunction cmf = new ChartMapFunction(center_coor, 1.0, 0.0);
								Position position = cmf.mapCoordinatesToXY(coor);
								star.setPosition(position);
							}

							return star;
						}
					}
				}
			} catch (EOFException e) {
				// All files have been read.
				if (current_file_index_in_list + 1 ==  file_list.size())
					return null;

				current_file_index = ((Integer)file_list.elementAt(current_file_index_in_list + 1)).intValue();

				URL url = null;
				try {
					url = url_set.existsFile(filenames[current_file_index]);
				} catch ( FileNotFoundException exception ) {
					url = url_set.existsFile(filenames[current_file_index] + ".dat");
				}
				BufferedReader reader = Decoder.newReader(url);

				// Closes the current file after confirming the next file can be open properly.
				if (current_reader != null)
					current_reader.close();
				current_file_index_in_list++;

				current_reader = reader;

				current_block_index = -1;
				current_star_index = 2000 - 1;
			}
		}
	}

	/**
	 * Closes a catalog. This method must be invoked finally.
	 * @exception IOException if a file cannot be accessed.
	 */
	public void close ( )
		throws IOException
	{
		current_reader.close();
		current_reader = null;
	}

